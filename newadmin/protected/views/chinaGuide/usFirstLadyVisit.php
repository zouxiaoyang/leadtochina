           <h2 style="background:url(/images/visit/no2.png) no-repeat;">Xian Terracotta Warriors</h2>
           <div class="newlist">
               <p>Xian, located in central-northwest China, records the great changes of the country just like a living history book. Called Chang'an (meaning the eternal city) in ancient times, it is one of the birthplaces of the ancient Chinese civilization in the Yellow River Basin area.</p>
               <p>The Terracotta Army is one of the top attractions in China, because of its historical significance and uniqueness. It is significant because the hundreds of detailed lifesize models represent the army that triumphed over all other Chinese armies and who were the decisive factor in forming a united China. </p>
               <p><span>Why Were the Terracotta Warriors Made?</span>
               The army of terracotta statues was made to be buried with First Emperor Qin Shihuang:<br />
               * As a show of his glory<br />
               * To remember the army that triumphed over the other Warring States to unite China<br />
               * Because it was believed that objects like statues can be animated in the afterlife, and because Qin Shihuang required an after-death army. </p>
               <p>The Terracotta Army Museum lies 2 km east of The Tomb of Qin Shihuang, known as the First Emperor, who unified China 2,200 years ago. Emperor Qin, from whom China gets its name, ordered the creation of this model army. It is a sight not to be missed by any visitor to China.</p>
