Traditional Chinese paintings can be classified as figure paintings, landscapes and flower-and-bird paintings. Landscapes represent a major category in traditional Chinese painting, mainly depicting the natural scenery of mountains and rivers. According to painting techniques, Chinese painting can be divided into two styles: xieyi style and gongbi style. Xieyi, or freehand, is marked by exaggerated forms and freehand brushwork. Gongbi, or meticulous, is characterized by close attention to detail and fine brushwork. Freehand painting generalizes shapes and displays rich brushwork and ink techniques. So, you are going to do freehand Chinese painting.</p>

