         <img src="/images/beijing_shanghai/seat1.jpg" alt="" />
         <p>This is a carriage of hard sleepers. You can see the carriage is divided into two areas by the aisle: area for sitting and area for sleeping. In the sleeping area, the opening compartment is divided into two rows by a small tray table; there are 3 beds in each row including 1 upper bed, 1 middle bed and 1 bottom bed. Hard sleeper bed is 60cm in width and 195cm in length on most traditional Chinese trains. The upper (top) bunk is very close to the ceiling which makes it very difficulty to sit up. There is no big difference between upper, middle and bottom beds and it's mainly about the position of the beds. Upper bed has the lowest price among all these three types of hard sleepers and the bottom bed has the highest price. Hard sleeper bed is rated well by the client, however, some people also point out that the hard sleeper carriage is a bit smoky.</p>
