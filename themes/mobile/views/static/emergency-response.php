        <p style="padding-top:20px;"><b>1) Motion Sickness (plane, boat, etc.)</b></p>
        <p class="emer_pt5">Car sickness, airsickness, seasickness are very common in travel. Those who has such problems should not eat too full before get on board. You better take some pills (it is best to prepared medicine under doctor instructions). </p>
        <p style="padding-top:20px;"><b>2) Heat Stroke</b></p>
        <p class="emer_pt5">The main symptoms of heat stroke is sweat, thirst, dizziness, tinnitus, vertigo, chest tightness, nausea, vomiting, fever, disorientation, and even coma. Prolonged stay in high-heat environment can easily cause heat stroke. So in summer, tourist should pay attention to avoid long time activities in the sun. </p>
        <p style="padding-top:20px;"><b>3) Heart Attack </b></p>
        <p class="emer_pt5">Prepare the medicine and take it under the instruction of your doctor's if you have heart disease. And, you are suggested to tell your concerns to your trip advisor and tour guide.</p>
        <div class="emer_quesbor">
       <p class="emer_pt5"> <img src="/images/faq/libg2.jpg" alt=""  align="absmiddle"   /> What should I do if I fall sick?</p>
        <p class="emer_pt5">Answer: If you feel minor discomforts, please relax, have a rest and drink some water. Eat more fruits and stay away from overly fatty foods. Take some medicine if necessary. If you suffer from a serious illness, please go to the hospital or call 120 (toll free) for an ambulance. Please remember to bring a copy of your medical records with you when you travel. Meanwhile, you are highly suggested to contact with your tour guide and report the situation to him/her who could give you timely assistance if it's necessary.</p></div>
        
