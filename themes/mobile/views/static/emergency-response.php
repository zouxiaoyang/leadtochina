     <h2 class="page-title16">Emergency Plan While Touring in China</h2>
     <div class="">
        <p class="emer_pt5">As an unavoidable appendant of the travelling, the unexpected emergencies can occasionally have serious consequences if we cannot foresee the problems and prepare ourselves with the solutions to handle them. In the following article, you will be armed with the most common emergencies and ways to solve with them. However, since the language barrier and the native advantages exist, Lead to China does recommend foreign visitors choose a travel agency that offers you timely and effective back-up services, which could save both your time and money. </p>
        <p class="emer_pt10">Meanwhile, some tricks do necessary and effective, such as travel with a bilingual map of your destination (major cities, such as Beijing, Xian, Shanghai, Guangzhou,etc., use bilingual signage to help visiors); Ask your tour guide to write down the address of your hotels or destination both in Chinese and English, better with Phone number, with which you can show to the taxi driver or sb. who can assist you back to hotel or destination; keep your guide's phone number and use it if it's necessary. Keep the phone number of your embassy or consulate in China, dial it if you find yourself in a serious situation.</p>
        <div class="emer_con1">
        <p style="padding:20px 0 0; margin:0;"><span class="emer_txt1">Lost Something</span></p>
        <p class="emer_pt10"><b>1) While travelling with your tour guide, report all lost items immediately to your guide, who knows where to find help, and who he/she should contact with.</b></p>
       <div class="emer_quesbor">
       <p class="emer_pt5"> <img src="/images/faq/libg2.jpg" alt=""     align="absmiddle"/> What to do if Ic annot find my travel guide?</p>
        <p class="emer_pt5">Answer: Waiting at the last meeting point; giving phone call to your tour guide/ trip advisor.</p></div>
        <p style="padding-top:20px;"><b>2) While travelling by yourself and in airport or train station, you can report the missing items at the nearest Lost and Found Center. When traveling by public transport, take down the vehicles/ registration numbers and company logos and call the police-the 110.</b></p>
       <div class="emer_quesbor">
       <p class="emer_pt5"> <img src="/images/faq/libg2.jpg" alt=""    align="absmiddle"/> What should I do if I lose my luggage in the airport?</p>
        <p class="emer_pt5">Answer: Report of the loss of your luggage at the relevant airport department. The office members will assist you to fill the report and provide you with the pictures of luggage which helps identify your luggage, and then they will also help you trace your luggage using computerised tracking systems and find best solution to get the luggage back.</p></div>
        <div class="emer_email"><img src="/images/Emergency-Response/email_img2.jpg" alt=""class="img-responsive"   /> </div>
        <p style="padding-top:20px;"><b>3) While staying at a hotel, report the missing items immediately to hotel and your tour guide, and they would help you immediately. If they failed by doing so, they would seek help from the 110.</b></p>
       <div class="emer_quesbor">
       <p class="emer_pt5"> <img src="/images/faq/libg2.jpg" alt=""     align="absmiddle"/> What should I do if I am stolen or robbed?</p>
        <p class="emer_pt5">Answer: If you lose something of value, please report immediately to the nearest police station or call 110 (toll free) for police. Notify your tour guide or tour leader for assistance. If you are in a hotel, please inform the hotel staff and ask them to call the police.</p></div>
<div class="emer_email"><img src="/images/Emergency-Response/email_img3.jpg"  class="img-responsive"   alt="" /> </div> 
       <p style="padding-top:20px;"><b>4) If your credit cards or traveler's checkes have been lost or stolen, call the issuer as soon as possible.</b> </p>
        <p style="padding-top:20px;"><b>5) If you lost your passport, you should report the lost asap to the nearest Public Security Bureau (your tour guide knows how to handle it) and to your embassy or consulate in China. The Police report about the lost is necessary for getting new visa from the Chinese authorities. Remember that the relevant departments are usually closed on Saturdays, Sundays and Chinese Holidays.</b></p>
